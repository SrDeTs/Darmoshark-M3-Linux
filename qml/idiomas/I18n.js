function languageCode(language) {
    if (!language)
        return "pt-BR"

    var value = String(language)
    if (value === "en-US" || value === "English" || value === "Inglês")
        return "en-US"
    if (value === "pt-BR" || value === "Português (Brasil)" || value === "Portuguese (Brazil)")
        return "pt-BR"
    return "pt-BR"
}

function tr(language, key) {
    var code = languageCode(language)
    var selected = dictionaries[code] || dictionaries["pt-BR"]
    if (selected[key] !== undefined)
        return selected[key]
    if (dictionaries["en-US"][key] !== undefined)
        return dictionaries["en-US"][key]
    return key
}
